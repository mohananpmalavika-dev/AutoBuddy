export function resolvePassengerLocale(languageCode) {
  const code = String(languageCode || 'en').trim().toLowerCase();
  if (code.startsWith('ml')) {
    return passengerDashboardLocales.ml;
  }
  return passengerDashboardLocales.en;
}

